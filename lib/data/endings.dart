  /// 全 ED の本文を返す（順序は [EndingKind.values] の宣言順）。
  static List<EndingBody> get all =>
      List<EndingBody>.unmodifiable(<EndingBody>[
        for (final k in EndingKind.values) _all[k]!,
      ]);

  /// 1 件取得（必須）。未定義 ED は例外（コンパイル時に全て揃えてある想定）。
  static EndingBody bodyOf(EndingKind kind) {
    final b = _all[kind];
    if (b == null) {
      throw StateError('EndingBody not defined for $kind');
    }
    return b;
  }
